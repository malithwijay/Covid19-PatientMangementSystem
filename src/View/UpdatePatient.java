    private void BtnAddPatientSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddPatientSaveActionPerformed
        // TODO add your handling code here:
        
        Pattern name = Pattern.compile("^[a-zA-Z ]*$");
        Matcher mname = name.matcher(TxtAddPatientName.getText());
        
        if(TxtAddPatientName.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Patient name cannot be blank!","Warning!", JOptionPane.WARNING_MESSAGE);
        }
        else if(mname.matches()==false){
            JOptionPane.showMessageDialog(this,"Its does not like a name!","Invalid Name!", JOptionPane.ERROR_MESSAGE);
        }
         else if(TxtAddPatientAdmittedDate.getDate()==null){
                        JOptionPane.showMessageDialog(this,"Admitted Date cannot be blank!","Warning!", JOptionPane.WARNING_MESSAGE);
        }
        else if(TxtAddPatientQperiodToDate.getDate()==null){
                         JOptionPane.showMessageDialog(this,"What's about the date this patient can exit?","Warning!", JOptionPane.WARNING_MESSAGE);

        }
        else{
            
            
        Date admittedDate = TxtAddPatientAdmittedDate.getDate();
        Date dischargeDate = TxtAddPatientDischargeDate.getDate();
        DateFormat dateformat =new SimpleDateFormat("yyyy-MM-dd");
        DatabaseController db = new DatabaseController();
        int i = db.insertUpdateDelete("update tb_patient set Name = '"+TxtAddPatientName.getText()+"',AdmittedDate='"+dateformat.format(admittedDate)+"',QuarantinePeriod='"+jLabel5.getText()+"',DischargeDate='"+dateformat.format(dischargeDate)+"'"
                + "where NIC='"+TxtAddPatientNic.getText()+"'");
        if(i==1)
            JOptionPane.showMessageDialog(this, "Patient Details Added Successfully","Done..!",JOptionPane.INFORMATION_MESSAGE);
        else
            JOptionPane.showMessageDialog(this, "Something Went Wrong While Saving Data","Error..!",JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_BtnAddPatientSaveActionPerformed

