    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        Pattern id = Pattern.compile("^([0-9]{9}[x|X|v|V]|[0-9]{12})$");
        Matcher mid = id.matcher(TxtAddPatientNic.getText());
        
        if(TxtAddPatientNic.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Patient NIC cannot be blank!","Warning!", JOptionPane.WARNING_MESSAGE);
        }
        else if(mid.matches()==false){
            JOptionPane.showMessageDialog(this,"Patient NIC is invalid. Please give valid NIC number!","Invalid NIC!", JOptionPane.ERROR_MESSAGE);
        }
        else{
            
            DatabaseController db = new DatabaseController();
        ResultSet rs = db.selectData("Select * from tb_patient where NIC = '"+TxtAddPatientNic.getText()+"'");
        if(rs!=null)
        {
            try {
                while(rs.next())
                {
                    TxtAddPatientNic.setText(rs.getString("NIC"));
                    TxtAddPatientName.setText(rs.getString("Name"));
                    jLabel5.setText(String.valueOf(rs.getInt("QuarantinePeriod")));
                    TxtAddPatientAdmittedDate.setDate(rs.getDate("AdmittedDate"));
                    TxtAddPatientDischargeDate.setDate(rs.getDate("DischargeDate"));
                    TxtAddPatientQperiodToDate.setDate(TxtAddPatientDischargeDate.getDate());
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error Occured while loading the data","Error..!",JOptionPane.ERROR_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "No Data Found","Warning..!",JOptionPane.WARNING_MESSAGE);
        }
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

